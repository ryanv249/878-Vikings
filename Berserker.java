package vikingbattle;

public class Berserker
{
	String description, phase;
	int numArmies, numMoves;
	
	public Berserker(String newDescription, String newPhase, int newNumArmies, int newNumMoves)
	{
		description = newDescription;
		phase = newPhase;
		numArmies = newNumArmies;
		numMoves = newNumMoves;
	}
}